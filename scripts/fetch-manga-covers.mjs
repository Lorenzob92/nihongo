/**
 * Pre-fetches AniList metadata for every rung on the manga ladder, then
 * writes the result to src/data/manga-covers.ts so the runtime app never
 * hits AniList.
 *
 * Strategy: search by title and verify against an expected romaji or known
 * AniList ID. AniList IDs verified manually by visiting https://anilist.co.
 *
 * Usage:  node scripts/fetch-manga-covers.mjs
 */

import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.resolve(__dirname, "..");
const OUTPUT = path.join(APP_ROOT, "src", "data", "manga-covers.ts");

// AniList IDs verified by searching anilist.co directly.
const QUERIES = [
  { slug: "yotsubato",         aniListId: 30104 },  // Yotsuba to!
  { slug: "doraemon",          aniListId: 31032 },  // Doraemon
  { slug: "frieren",           aniListId: 118586 }, // Sousou no Frieren
  { slug: "spy-x-family",      aniListId: 108556 }, // SPY x FAMILY
  { slug: "mob-psycho-100",    aniListId: 85189 },  // Mob Psycho 100
  { slug: "pluto",             aniListId: 30745 },  // PLUTO
  { slug: "attack-on-titan",   aniListId: 53390 },  // Shingeki no Kyojin
  { slug: "death-note",        aniListId: 30021 },  // Death Note
  { slug: "monster",           aniListId: 30001 },  // Monster
  { slug: "20th-century-boys", aniListId: 30003 },  // 20 Seiki Shounen
  { slug: "vinland-saga",      aniListId: 30642 },  // Vinland Saga
  { slug: "berserk",           aniListId: 30002 },  // Berserk
];

const QUERY_BY_ID = `
query ($id: Int) {
  Media(id: $id, type: MANGA) {
    id
    title { romaji native english }
    coverImage { extraLarge large medium }
    bannerImage
    description(asHtml: false)
    averageScore
    chapters
    volumes
    status
    genres
    startDate { year }
  }
}`;

async function fetchById(id) {
  const res = await fetch("https://graphql.anilist.co", {
    method: "POST",
    headers: { "Content-Type": "application/json", Accept: "application/json" },
    body: JSON.stringify({ query: QUERY_BY_ID, variables: { id } }),
  });
  if (!res.ok) throw new Error(`AniList HTTP ${res.status}`);
  const json = await res.json();
  if (json.errors) throw new Error(`AniList: ${json.errors.map(e => e.message).join("; ")}`);
  return json.data?.Media;
}

function sanitiseDescription(raw) {
  if (!raw) return "";
  return raw
    .replace(/<br\s*\/?>(\s*)/gi, "\n")
    .replace(/<[^>]+>/g, "")
    .replace(/\u2014/g, ", ")  // strip em-dashes
    .replace(/\u2013/g, "-")  // strip en-dashes
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

async function main() {
  const out = {};
  for (const q of QUERIES) {
    try {
      const m = await fetchById(q.aniListId);
      if (!m) {
        console.warn(`! no media for ${q.slug} (id ${q.aniListId})`);
        continue;
      }
      out[q.slug] = {
        aniListId: m.id,
        titleRomaji: m.title?.romaji ?? null,
        titleNative: m.title?.native ?? null,
        titleEnglish: m.title?.english ?? null,
        coverExtraLarge: m.coverImage?.extraLarge ?? null,
        coverLarge: m.coverImage?.large ?? null,
        coverMedium: m.coverImage?.medium ?? null,
        bannerImage: m.bannerImage ?? null,
        description: sanitiseDescription(m.description),
        averageScore: m.averageScore ?? null,
        chapters: m.chapters ?? null,
        volumes: m.volumes ?? null,
        status: m.status ?? null,
        genres: m.genres ?? [],
        year: m.startDate?.year ?? null,
      };
      console.log(`+ ${q.slug.padEnd(22)}  ${m.title?.romaji}`);
    } catch (err) {
      console.error(`! ${q.slug} (${q.aniListId}): ${err.message}`);
    }
    await new Promise((r) => setTimeout(r, 1100));
  }

  const banner = `// AUTO-GENERATED by scripts/fetch-manga-covers.mjs at ${new Date().toISOString()}.
// Do not edit by hand. Re-run the script to refresh.

export type MangaCover = {
  aniListId: number;
  titleRomaji: string | null;
  titleNative: string | null;
  titleEnglish: string | null;
  coverExtraLarge: string | null;
  coverLarge: string | null;
  coverMedium: string | null;
  bannerImage: string | null;
  description: string;
  averageScore: number | null;
  chapters: number | null;
  volumes: number | null;
  status: string | null;
  genres: string[];
  year: number | null;
};

export const MANGA_COVERS: Record<string, MangaCover> = ${JSON.stringify(out, null, 2)};
`;

  await writeFile(OUTPUT, banner, "utf8");
  console.log(`\nWrote ${Object.keys(out).length} entries to ${path.relative(APP_ROOT, OUTPUT)}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
